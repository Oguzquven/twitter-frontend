// onPost: tweet gönder fonksiyonu
// hasContent: içerik var mı (buton aktif/pasif)
// loading: gönderiliyor mu
function Toolbar({ isFocused, onPost, hasContent, loading }) {
  return (
    <div
      style={{
        display: "flex",
        alignItems: "center",
        justifyContent: "space-between",
        paddingTop: isFocused ? "12px" : "0",
        borderTop: isFocused ? "1px solid #2f3336" : "none",
      }}
    >
      {/* Sol: Toolbar ikonları */}
      <div style={{ display: "flex", gap: "4px" }}>
        {toolbarIcons.map((item, i) => (
          <div
            key={i}
            style={{
              width: "34px",
              height: "34px",
              borderRadius: "50%",
              display: "flex",
              alignItems: "center",
              justifyContent: "center",
              cursor: "pointer",
              color: "#1d9bf0",
              transition: "background 0.2s",
            }}
            onMouseEnter={(e) =>
              (e.currentTarget.style.backgroundColor = "rgba(29,155,240,0.1)")
            }
            onMouseLeave={(e) =>
              (e.currentTarget.style.backgroundColor = "transparent")
            }
          >
            {item.icon}
          </div>
        ))}
      </div>

      {/* Sağ: Gönderi yayınla butonu - içerik varsa mavi olur */}
      <button
        onClick={onPost}
        disabled={!hasContent || loading}
        style={{
          padding: "8px 16px",
          backgroundColor: hasContent && !loading ? "#1d9bf0" : "#787878",
          color: "#ffffff",
          border: "none",
          borderRadius: "9999px",
          fontSize: "15px",
          fontWeight: "700",
          cursor: hasContent && !loading ? "pointer" : "not-allowed",
          transition: "background 0.2s",
        }}
        onMouseEnter={(e) =>
          hasContent &&
          !loading &&
          (e.currentTarget.style.backgroundColor = "#1a8cd8")
        }
        onMouseLeave={(e) =>
          hasContent &&
          !loading &&
          (e.currentTarget.style.backgroundColor = "#1d9bf0")
        }
      >
        {loading ? "Gönderiliyor..." : "Gönderi yayınla"}
      </button>
    </div>
  );
}

export default Toolbar;
